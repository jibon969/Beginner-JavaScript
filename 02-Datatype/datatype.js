// Javascript has 4 datatype
/*
    1. string
    2. number
    3. boolean
    4. undefined
    5. null
*/

// Example of string
var x = "Hello World"
console.log(x);
console.log(typeof (x));

// Example of number
var i = 10;
console.log(i);
console.log(typeof (i));

// Example of boolean
var x = true;
console.log(x);
console.log(typeof (x));

// Example of null
var x = null;
console.log(x);
console.log(typeof (x));